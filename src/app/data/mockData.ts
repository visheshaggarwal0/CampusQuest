// ─── PROJECT APPLICATION TRACKING ───────────────────────
export interface ProjectApplication {
  projectId: string;
  studentId: string;
  status: ProjectStatus;
  appliedDate: string;
  rating?: number; // Only for completed projects
  hoursSpent?: number; // Only for completed/in-progress projects
}

// Initialize with empty applications - will be built as users interact
export const projectApplications: ProjectApplication[] = [];

// ─── HELPER FUNCTIONS ─────────────────────────────────────
export function getAccountByEmail(email: string) {
  const student = students.find((s) => s.email === email);
  if (student) return student;
  const prof = faculty.find((f) => f.email === email);
  if (prof) return prof;
  const club = clubs.find((c) => c.email === email);
  return club ?? null;
}

export function getProjectsByPoster(posterId: string) {
  return projects.filter((p) => p.posterId === posterId);
}

export function getApplicantsForProject(projectId: string): ProjectApplication[] {
  return projectApplications.filter((app) => app.projectId === projectId);
}

export function getProjectsForStudent(studentId: string): ProjectApplication[] {
  return projectApplications.filter((app) => app.studentId === studentId);
}

export function updateStudentStats(studentId: string) {
  const student = students.find((s) => s.id === studentId);
  if (!student) return;

  const studentProjects = getProjectsForStudent(studentId);

  student.projectsCompleted = studentProjects.filter((p) => p.status === "completed").length;
  student.activeProjects = studentProjects.filter(
    (p) => p.status === "in-progress" || p.status === "selected"
  ).length;

  const completedProjects = studentProjects.filter((p) => p.status === "completed" && p.rating);
  if (completedProjects.length > 0) {
    const totalRating = completedProjects.reduce((sum, p) => sum + (p.rating || 0), 0);
    student.avgRating = totalRating / completedProjects.length;
  } else {
    student.avgRating = 0;
  }

  student.hoursLogged = studentProjects.reduce((sum, p) => sum + (p.hoursSpent || 0), 0);

  // Credibility calculation: 100 base + (completed * 50) + (avg rating * 100)
  student.credibilityScore = Math.round(
    100 +
    (student.projectsCompleted * 50) +
    (student.avgRating * 100)
  );
}

export function updateProjectApplicantCount(projectId: string) {
  const project = projects.find((p) => p.id === projectId);
  if (!project) return;

  const applicants = getApplicantsForProject(projectId);
  project.applicants = applicants.length;
}

export function updateFacultyProjectCount(facultyId: string) {
  const prof = faculty.find((f) => f.id === facultyId);
  if (!prof) return;

  prof.projectsPosted = projects.filter((p) => p.posterId === facultyId).length;
}

export function updateClubProjectCount(clubId: string) {
  const club = clubs.find((c) => c.id === clubId);
  if (!club) return;

  club.projectsPosted = projects.filter((p) => p.posterId === clubId).length;
}
